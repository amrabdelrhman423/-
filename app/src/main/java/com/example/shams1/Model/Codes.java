package com.example.shams1.Model;

public class Codes {


    String code ,descount;

    public Codes() {
    }

    public Codes(String code, String descount) {
        this.code = code;
        this.descount = descount;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDescount() {
        return descount;
    }

    public void setDescount(String descount) {
        this.descount = descount;
    }
}
